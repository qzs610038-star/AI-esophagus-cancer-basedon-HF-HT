#!/usr/bin/env python3
"""
finalize_experiment.py — Finalize a completed PFMval experiment.

Reads training_history.csv, finds the best epoch by val_loss minimum,
updates experiment_registry.json, regenerates experiment_dashboard.md.

Usage:
    python scripts/finalize_experiment.py --run-dir <path> --experiment-id <id>
    python scripts/finalize_experiment.py --run-dir <path> --experiment-id <id> --baseline-id <baseline_id>
    python scripts/finalize_experiment.py --run-dir <path> --experiment-id <id> --mode server
    python scripts/finalize_experiment.py --run-dir <path> --experiment-id <id> --no-val  # Strategy A (no val_loss)

Does NOT auto-modify CLAUDE.md — outputs sync suggestions only.
"""

import argparse
import csv
import json
import os
import sys
from datetime import datetime, timezone
from pathlib import Path


PROJECT_ROOT = Path(__file__).resolve().parent.parent
REGISTRY_PATH = PROJECT_ROOT / "experiments" / "experiment_registry.json"
DASHBOARD_PATH = PROJECT_ROOT / "experiments" / "experiment_dashboard.md"


def load_registry():
    """Load experiment registry JSON."""
    if not REGISTRY_PATH.exists():
        print(f"[FAIL] Registry not found: {REGISTRY_PATH}")
        sys.exit(1)
    with open(REGISTRY_PATH, "r", encoding="utf-8") as f:
        return json.load(f)


def save_registry(registry):
    """Save experiment registry JSON."""
    registry["updated_at"] = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S")
    with open(REGISTRY_PATH, "w", encoding="utf-8") as f:
        json.dump(registry, f, indent=2, ensure_ascii=False)
        f.write("\n")


def load_training_history(run_dir):
    """Load training_history.csv. Returns list of dicts."""
    csv_path = Path(run_dir) / "training_history.csv"
    if not csv_path.exists():
        return None, str(csv_path)
    with open(csv_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        return list(reader), str(csv_path)


def find_best_epoch(rows, metric_column="val_loss"):
    """Find the row with minimum val_loss. Returns (best_row, train_val_gap)."""
    best = min(rows, key=lambda r: float(r[metric_column]))
    train_pcc = float(best.get("train_pcc", 0))
    val_pcc = float(best.get("val_pcc", 0))
    train_val_gap = train_pcc - val_pcc
    return best, train_val_gap


def generate_dashboard(registry):
    """Generate experiment_dashboard.md from registry."""
    experiments = registry.get("experiments", [])

    status_icon = {
        "done": "white_check_mark",
        "done_incomplete_data": "warning",
        "running": "green_circle",
        "planned": "large_blue_circle",
        "paused": "pause_button",
        "failed": "red_circle",
    }

    # Sort by priority then status
    priority_order = {"P0": 0, "P1": 1, "P2": 2, "P3": 3, "baseline": 4}
    experiments_sorted = sorted(
        experiments,
        key=lambda e: (priority_order.get(e.get("priority", ""), 99), e.get("status", ""))
    )

    lines = []
    lines.append("# Experiment Dashboard")
    lines.append("")
    lines.append(f"> Auto-generated by `finalize_experiment.py`. Last updated: {registry['updated_at']}.")
    lines.append("> Source of truth: `experiments/experiment_registry.json`")
    jfx_status = registry.get("jfx_data_status", {})
    if jfx_status:
        lines.append(">")
        if jfx_status.get("status") in {"synced_processed", "completed_both_sides"}:
            validation = jfx_status.get("prep_validation") or jfx_status.get("local", {}).get("validation", "validation passed")
            next_action = jfx_status.get("next_action", "review JFX cache/retraining plan")
            lines.append(
                "> 🟢 **JFX0729 数据更正状态**：更正数据已同步、本地处理并通过 "
                f"{validation}；旧 JFX 相关结果仅作历史参考。下一步：{next_action}。"
            )
        else:
            next_action = jfx_status.get("next_action", "review JFX data status")
            lines.append(
                "> 🚨 **JFX0729 数据状态**：registry 标记尚未完成闭环。"
                f"下一步：{next_action}。"
            )
    lines.append("")

    # Status overview table
    lines.append("## Status Overview")
    lines.append("")
    lines.append("| ID | Family | Status | Fold | Encoder | Tokens | Best Val PCC | Best Val Loss | Train-Val Gap | Epoch |")
    lines.append("|----|--------|--------|:----:|---------|:------:|:------------:|:-------------:|:-------------:|:-----:|")

    for exp in experiments_sorted:
        eid = exp["id"]
        family = exp.get("family", "")
        status = exp.get("status", "unknown")
        fold = exp.get("fold", "—")
        encoder = exp.get("encoder_type", "")
        tokens = exp.get("num_tokens", "—")
        pcc = f"{exp['best_val_pcc']:.4f}" if exp.get("best_val_pcc") is not None else "—"
        loss = f"{exp['best_val_loss']:.4f}" if exp.get("best_val_loss") is not None else "—"
        gap = f"{exp.get('train_val_gap', 0):.4f}" if exp.get("train_val_gap") is not None else "—"
        epoch = str(exp.get("best_epoch", "—")) if exp.get("best_epoch") is not None else "—"

        icon = status_icon.get(status, "question")
        note = ""
        if exp.get("external_xzy_pcc") is not None:
            note = f" | ext_XZY_PCC={exp['external_xzy_pcc']:.4f}"
        lines.append(f"| {eid} | {family} | :{icon}: {status} | {fold} | {encoder} | {tokens} | {pcc} | {loss} | {gap} | {epoch}{note} |")

    lines.append("")

    # Priority queue
    lines.append("## Priority Queue")
    lines.append("")
    lines.append("| Priority | ID | Status | Next Action |")
    lines.append("|:--------:|----|--------|-------------|")

    for exp in experiments_sorted:
        priority = exp.get("priority", "—")
        eid = exp["id"]
        status = exp.get("status", "unknown")
        action = exp.get("next_action", "—")
        lines.append(f"| {priority} | {eid} | {status} | {action} |")

    lines.append("")

    # Decision gates
    gates = [e for e in experiments_sorted if e.get("decision_gate")]
    if gates:
        lines.append("## Decision Gates")
        lines.append("")
        lines.append("| Experiment | Gate |")
        lines.append("|------------|------|")
        for exp in gates:
            lines.append(f"| {exp['id']} | {exp['decision_gate']} |")
        lines.append("")

    with open(DASHBOARD_PATH, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    print(f"[INFO] Dashboard regenerated: {DASHBOARD_PATH}")


def main():
    parser = argparse.ArgumentParser(description="Finalize a PFMval experiment")
    parser.add_argument("--run-dir", required=True, help="Path to experiment run directory")
    parser.add_argument("--experiment-id", required=True, help="Experiment ID in registry")
    parser.add_argument("--registry", default=str(REGISTRY_PATH), help="Path to experiment_registry.json")
    parser.add_argument("--baseline-id", default=None, help="Baseline experiment ID for delta comparison")
    parser.add_argument("--mode", default="local", choices=["local", "server"],
                        help="local: warn on missing CSV; server: fail on missing CSV")
    parser.add_argument("--no-val", action="store_true",
                        help="Strategy A: use final epoch (no val_loss column in CSV)")
    parser.add_argument("--external-xzy-pcc", type=float, default=None,
                        help="External XZY mean PCC (Strategy A, manually added after training)")
    args = parser.parse_args()

    run_dir = Path(args.run_dir)
    if not run_dir.is_absolute():
        run_dir = PROJECT_ROOT / run_dir

    # ----------------------------------------------------------
    # Step 1: Load training_history.csv
    # ----------------------------------------------------------
    rows, csv_path = load_training_history(run_dir)

    if rows is None:
        msg = f"[WARN] training_history.csv not found at: {csv_path}"
        if args.mode == "server":
            print(f"[FAIL] {msg}")
            print("[FAIL] Server mode: missing CSV is fatal. Exiting.")
            sys.exit(1)
        else:
            print(msg)
            print("[INFO] Local mode: CSV not available (likely server-trained).")
            print("[INFO] Cannot finalize — no data to select best epoch.")
            print("[INFO] Exit 0 (no error). Registry NOT updated.")
            sys.exit(0)

    if len(rows) == 0:
        print(f"[FAIL] training_history.csv is empty: {csv_path}")
        sys.exit(1)

    # ----------------------------------------------------------
    # Step 2: Find best epoch (val_loss minimum) OR use final epoch (--no-val)
    # ----------------------------------------------------------
    if args.no_val:
        # Strategy A: no validation set. Prefer the explicit best epoch written
        # by train_mpp_uni2h_mlp.py (early-stopping-aware: best_epoch.txt or
        # the `is_best` column), consistent with best_checkpoint.pth.
        # Fall back to rows[-1] (final-epoch) for legacy runs without these markers.
        best_epoch_file = Path(run_dir) / "best_epoch.txt"
        best_row = None
        if best_epoch_file.exists():
            with open(best_epoch_file, "r", encoding="utf-8") as f:
                for line in f:
                    if line.startswith("best_epoch="):
                        be = int(line.split("=", 1)[1].strip())
                        best_row = next((r for r in rows if int(r["epoch"]) == be), None)
                        if best_row is not None:
                            best_epoch = be
                        break
        if best_row is None:
            for r in rows:
                if r.get("is_best", "").strip().lower() in ("true", "1", "yes"):
                    best_row = r
                    best_epoch = int(best_row["epoch"])
                    break
        if best_row is None:
            best_row = rows[-1]
            best_epoch = int(best_row["epoch"])
            print(f"[WARN] No best_epoch.txt / is_best marker found; "
                  f"falling back to FINAL epoch (epoch={best_epoch})")
        best_val_loss = None
        best_val_pcc = None
        train_val_gap = None
        train_pcc = float(best_row.get("train_pcc", 0)) if best_row.get("train_pcc") else None

        print(f"[INFO] Loaded {len(rows)} epochs from {csv_path}")
        print(f"[INFO] --no-val mode: using BEST checkpoint (epoch={best_epoch})")
        print(f"       train_loss={best_row.get('train_loss', 'N/A')}  train_pcc={train_pcc or 'N/A'}")
    else:
        # Standard mode: select best epoch by val_loss minimum
        best_row, train_val_gap = find_best_epoch(rows, "val_loss")
        best_epoch = int(best_row["epoch"])
        best_val_loss = float(best_row["val_loss"])
        best_val_pcc = float(best_row["val_pcc"])
        train_pcc = float(best_row["train_pcc"])

        print(f"[INFO] Loaded {len(rows)} epochs from {csv_path}")
        print(f"[INFO] Best epoch by val_loss_min: epoch={best_epoch}")
        print(f"       val_loss={best_val_loss:.4f}  val_pcc={best_val_pcc:.4f}  train_pcc={train_pcc:.4f}")
        print(f"       train_val_gap={train_val_gap:.4f}")

    # ----------------------------------------------------------
    # Step 3: Update registry
    # ----------------------------------------------------------
    registry = load_registry()
    experiments = registry.get("experiments", [])
    target = None
    for exp in experiments:
        if exp["id"] == args.experiment_id:
            target = exp
            break

    if target is None:
        print(f"[FAIL] Experiment '{args.experiment_id}' not found in registry: {args.registry}")
        sys.exit(1)

    target["status"] = "done"
    target["best_epoch"] = best_epoch if best_epoch is not None else "final"
    target["best_val_loss"] = best_val_loss
    target["best_val_pcc"] = best_val_pcc
    target["train_val_gap"] = round(train_val_gap, 6) if train_val_gap is not None else None
    if args.external_xzy_pcc is not None:
        target["external_xzy_pcc"] = round(args.external_xzy_pcc, 6)
    target["completed_at"] = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S")
    target["next_action"] = "review_results"

    save_registry(registry)
    print(f"[PASS] Registry updated: {args.experiment_id} -> done")

    # ----------------------------------------------------------
    # Step 4: Baseline comparison (optional)
    # ----------------------------------------------------------
    if args.baseline_id and not args.no_val and best_val_pcc is not None:
        baseline = None
        for exp in experiments:
            if exp["id"] == args.baseline_id:
                baseline = exp
                break
        if baseline and baseline.get("best_val_pcc") is not None:
            delta_pcc = best_val_pcc - baseline["best_val_pcc"]
            direction = "+" if delta_pcc >= 0 else ""
            print(f"\n[INFO] Baseline Comparison:")
            print(f"       Baseline ({args.baseline_id}): PCC={baseline['best_val_pcc']:.4f}")
            print(f"       Current  ({args.experiment_id}): PCC={best_val_pcc:.4f}")
            print(f"       Delta PCC: {direction}{delta_pcc:.4f}")

            # Check decision gate
            gate = target.get("decision_gate", "")
            if ">=" in gate and "best_val_pcc" in gate:
                try:
                    threshold_str = gate.split(">=")[1].strip().split()[0]
                    threshold = float(threshold_str)
                    if best_val_pcc >= threshold:
                        print(f"       [GATE PASS] best_val_pcc ({best_val_pcc:.4f}) >= threshold ({threshold:.4f})")
                    else:
                        print(f"       [GATE FAIL] best_val_pcc ({best_val_pcc:.4f}) < threshold ({threshold:.4f})")
                except (ValueError, IndexError):
                    pass
        else:
            print(f"[WARN] Baseline '{args.baseline_id}' not found or has no val_pcc; skipping comparison.")

    # ----------------------------------------------------------
    # Step 5: Regenerate dashboard
    # ----------------------------------------------------------
    generate_dashboard(registry)

    # ----------------------------------------------------------
    # Step 6: Sync suggestions (human-in-the-loop)
    # ----------------------------------------------------------
    print(f"\n{'='*60}")
    print(f"[INFO] Suggested manual sync actions:")
    print(f"  1. Update CLAUDE.md if best-PCC ranking changed")
    print(f"  2. Update .qoder/experience.md performance baseline table")
    print(f"  3. Update .claude/next-steps.md priority queue")
    print(f"  4. Update .claude/session-brief.md current task status")
    print(f"  5. Run: python scripts/check_project_state.py --mode local")
    print(f"  6. Commit registry + dashboard if results should sync to server:")
    print(f"     git add experiments/experiment_registry.json experiments/experiment_dashboard.md")
    print(f"{'='*60}")


if __name__ == "__main__":
    main()
